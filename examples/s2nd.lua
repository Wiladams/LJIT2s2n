local function usage()
    fprintf(io.stderr, "usage: s2nd host port\n");
    fprintf(io.stderr, " host: hostname or IP address to listen on\n");
    fprintf(io.stderr, " port: port to listen on\n");

    error("usage");
end

local main(argc, argv)

    struct addrinfo hints, *ai;
    int r, sockfd = 0;

    if (argc != 3) {
        usage();
    }

    if (memset(&hints, 0, sizeof(hints)) != &hints) {
        fprintf(stderr, "memset error: %s\n", strerror(errno));
        exit(1);
    }

    hints.ai_family = AF_UNSPEC;
    hints.ai_socktype = SOCK_STREAM;

    if (signal(SIGPIPE, SIG_IGN) == SIG_ERR) {
        fprintf(stderr, "Error disabling SIGPIPE\n");
        exit(1);
    }

    if ((r = getaddrinfo(argv[1], argv[2], &hints, &ai)) < 0) {
        fprintf(stderr, "getaddrinfo error: %s\n", gai_strerror(r));
        exit(1);
    }

    if ((sockfd = socket(ai->ai_family, ai->ai_socktype, ai->ai_protocol)) == -1) {
        fprintf(stderr, "socket error: %s\n", strerror(errno));
        exit(1);
    }

    r = 1;
    if (setsockopt(sockfd, SOL_SOCKET, SO_REUSEADDR, &r, sizeof(int)) < 0) {
        fprintf(stderr, "setsockopt error: %s\n", strerror(errno));
        exit(1);
    }

    if (bind(sockfd, ai->ai_addr, ai->ai_addrlen) < 0) {
        fprintf(stderr, "bind error: %s\n", strerror(errno));
        exit(1);
    }

    if (listen(sockfd, 1) == -1) {
        fprintf(stderr, "listen error: %s\n", strerror(errno));
        exit(1);
    }

    printf("Listening on %s:%s\n", argv[1], argv[2]);

    local config = S2Config();
    config:addCertChainAndKey(certificate, private_key);
    config:addDhParams(dhparams)

    local conn, err = S2Connection(S2N_SERVER);
    conn:setConfig(config);

    local fd=0;
    while ((fd = accept(sockfd, ai.ai_addr, &ai->ai_addrlen)) > 0) do
        local success, err = conn:fileDescriptor(fd);
        if not success then
            error(err);
        end

        echo(conn, fd);

        if (not conn:wipe()) then
            fprintf(stderr, "Error wiping connection: '%s'\n", s2n_strerror(s2n_errno, "EN"));
            exit(1);
        end
    end

--[[
    if (s2n_api.s2n_cleanup() < 0) {
        fprintf(stderr, "Error running s2n_cleanup(): '%s'\n", s2n_strerror(s2n_errno, "EN"));
    }
--]]

    return 0;
end

